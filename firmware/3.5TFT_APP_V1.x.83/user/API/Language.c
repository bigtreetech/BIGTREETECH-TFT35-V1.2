bool isRussia(const u8 *str)
{
	if(str[0]==0xA7)
	{
		if(str[1]>=0xA1&&str[1]<=0xF1)
		return true;
	}
	return false;
}

bool isArmenian(const u8 *str)
{
	if(str[0]==0xA1)
	{
		if(str[1]>=0x40&&str[1]<=0x8B)
		return true;
	}
	return false;
}



u16 my_strlen(const u8 *str)
{
	u16 i=0,ri=0;
	
	while(str[i])
	{
		if(isRussia(str+i) || isArmenian(str+i))
		{
			i+=2;
		}
		else
		{
			i++;
		}
		ri++;
	}
	return ri;
}

u8 * textSelect(u8 sel)
{
	switch(infoSetting.language)
	{
		case ENGLISH:			return en_str[sel];
		case CHINESE:			return cn_str[sel];
		case RUSSIAN:			return ru_str[sel];
		case JAPANESE:		    return jp_str[sel];
		case ARMENIAN:		    return armenian_str[sel];
		default:                return 0;					 
	}
}



